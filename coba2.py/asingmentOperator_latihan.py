# operasi yg dapat dilakukan dengan penyingkatan

a = 5 # adalah assignment

a += 1 # artinya addalah a= a + 1
print(f'nilai a += 1, nilai a menjadi {a}')

a -= 1 # artinya adalah a = a - 1
print(f'nilai a -= 1, nilai a menjadi {a}')


a *= 2 # artinya adalah a = a * 2
print(f'nilai a *= 2, nilai a menjadi {a}')


a /= 2 # artinya adalah a = a / 2
print(f'nilai a /= 2, nilai a menjadi {a}')

b = 10
print(f'nilai b = {b}')

b %= 3
print(f'nilai b %= 3, nilai b menjadi {b}')

b = 10
print(f'nilai b = {b}')

b //= 3
print(f'nilai b //= 3, nilai b menjadi {b}')

b = 10
